                    :disabled="loading"
                  >
                    📷 上传二维码
                  </button>
                </div>
                
                <div class="update-time">
                  <span class="time-label">最后更新:</span>
                  <span class="time-value">{{ formatUpdateTime(timeOption.lastUpdate) }}</span>
                </div>
              </div>
              
              <!-- 显示已上传的二维码 -->
              <div v-if="timeOption.qrCode" class="qr-preview">
